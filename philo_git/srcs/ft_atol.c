#include "../incs/philo.h"

long	ft_atol(char *s)
{
	int		sign;
	long	res;

	sign = 1;
	res = 0;
	while (*s == ' ' || (*s >= 9 && *s <= 13))
		s++;
	if (*s == '-' || *s == '+')
	{
		if (*s == '-')
			sign = -1;
		s++;
	}
	while (*s)
	{
		res = res * 10 + (*s - '0');
		s++;
	}
	return (res * sign);
}
